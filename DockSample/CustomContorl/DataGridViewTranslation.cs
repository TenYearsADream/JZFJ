        #region 合计行
        public static void TotalRow(DataGridView dg,List<string> colname)
        {
            dg.Rows.Add();
            DataGridViewRow dgr = dg.Rows[dg.Rows.Count - 1];
            dgr.ReadOnly = true;
            dgr.DefaultCellStyle.BackColor = System.Drawing.Color.Khaki;
            dgr.Cells[0].Value = "合计";
            for (int i = 0; i < dg.Rows.Count - 1; i++)
            {
                foreach (string i1 in colname)
                {
                    dgr.Cells[i1].Value = Convert.ToSingle(dgr.Cells[i1].Value) + Convert.ToSingle(dg.Rows[i].Cells[i1].Value);
                }
            }
        }
